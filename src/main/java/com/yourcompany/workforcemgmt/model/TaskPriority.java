package com.yourcompany.workforcemgmt.model;

/**
 * Enumeration representing the priority levels of a task
 */
public enum TaskPriority {
    /**
     * Low priority task - can be completed when time permits
     */
    LOW,
    
    /**
     * Medium priority task - standard priority level
     */
    MEDIUM,
    
    /**
     * High priority task - requires immediate attention
     */
    HIGH
}